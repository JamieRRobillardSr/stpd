def divide_it(x):
    """
    Return x divided by 2 as an integer
    :param x: int
    :return: int(x / 2)
    """
    return int(x / 2)

def multiply_it(x):
    """
    Return x mulitiplied by 4 as an integer
    :param x: int
    :return: int(x * 4)
    """
    return int(x * 4)

x = divide_it(10)
multiply_it(x)
